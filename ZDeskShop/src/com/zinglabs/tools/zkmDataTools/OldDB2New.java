	public void getFolderFiles(File file,List list){
		if(file.isDirectory()){
			File [] fs=file.listFiles();
			if(fs.length>0){
				for(File f:fs){
					if(f.isDirectory()){
						getFolderFiles(f,list);
					}else{
						list.add(f);
					}
				}
			}
		}else{
			list.add(file);
		}
	}
	
